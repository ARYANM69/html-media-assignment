// Ques: WAP to find the circumference of a circle with radius 10 in C++.
// Answer
#include <iostream>

using namespace std;

int main() {
    float radius = 10.0;      
    float pi = 3.14159;      
    float circumference  = 2 * pi * radius;;    
  

    // Output the result
    cout << "The circumference of the circle with radius " << radius << " is " << circumference << endl;

    return 0;  
}





